window.onload = function() {
var ribbon = document.createElement('a');
ribbon.setAttribute('href', 'http://www.1955-2011.org/');
ribbon.setAttribute('style', 'position:absolute;top:0;right:0;border:0;');
var ribbonImage = document.createElement('img');
ribbonImage.setAttribute('src', 'http://www.1955-2011.org/static/ribbon.png');
ribbon.appendChild(ribbonImage);
document.body.appendChild(ribbon);
}
